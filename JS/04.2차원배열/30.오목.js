// 방향을 정해서만 살펴보면 된다.
function solution(board) {
	let answer = 0;
	let map = Array(21).fill(0);
	for (let i = 1; i <= 19; i++) {
		for (let j = 1; j <= 19; j++) {
			map[i][j] = board[i - 1][j - 1];
		}
	}
	console.log(board[3][2]);

	let dx = [-1, 0, 1, 1];
	let dy = [1, 1, 1, 0];
	//방향 설정하기
	for (let i = 1; i <= 19; i++) {
		for (let j = 1; j <= 19; j++) {
			if (map[i][j] !== 1 && map[i][j] !== 2) {
				let cnt = 1;
				for (let k = 0; k < 4; k++) {
					if (map[i - dx[k]][j - dy[k]] !== board[i][j]) {
						while (true) {
							xx = i + dx[k];
							yy = j + dy[k];
							if (map[xx][yy] === board[i][j]) cnt++;
							else break;
						}
					}
					if (cnt === 5) {
						answer = map[i][j];
						return answer;
					}
				}
			}
		}
	}
	return answer;
}

